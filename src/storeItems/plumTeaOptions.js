const priced = [...page1, ...page2]
  .map(({name, price, img}) => ({
    name,
    img,
    price: Number(price.split(/\$|\//g)[1])
  }))
  .sort((a,b) => a.price - b.price);

export const minPrice = priced.map(({price}) => (price))
  .reduce((a,b) => Math.min(a,b));

const varietyOptions = priced.reduce(({options, upcharge, imgs}, {name, img, price}) => {
  options.push(name);
  upcharge.push(price - minPrice);
  imgs.push(img);
  return {
    options,
    upcharge,
    imgs,
  }
}, { options: [], imgs: [], upcharge: []});

